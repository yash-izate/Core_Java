package basic;

public class Operators {
    public static void main(String[] args) {

        double a = 10.3333;
        double b = 7.7723;

        System.out.println("Arithmetic Operator");

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
    }
}
